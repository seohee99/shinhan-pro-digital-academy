public class EX02_Switch {
    public static void main(String[] args) {

        int num = 6;

        switch (num){
            case 9:
                System.out.println("A");
                break;
            case 8:
                System.out.println("B");
                break;
            case 7:
                System.out.println("C");
                break;
            default:
                System.out.println("F");
        }

    }
}
